
def bubbleSort(array):
    n = len(array)
    pred = 0
    suc = 1
    
    if len(array)==1 or len(array)==0:
        return array
    
    while True:
            
        while suc < n:
            if array[pred]>array[suc]:
                a = array[pred]
                array[pred] = array[suc]
                array[suc] = a
                pred += 1
                suc += 1

            else:
                pred += 1
                suc += 1
                
        n -= 1
        pred = 0
        suc = 1
        
        if n == 1:
            break
        
    return array

